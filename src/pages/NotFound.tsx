import { useLocation } from "react-router-dom";
import { useEffect } from "react";
import { useTranslation } from "react-i18next";
import { logger } from '@/lib/logger';

const NotFound = () => {
  const location = useLocation();
  const { t } = useTranslation();

  useEffect(() => {
    logger.warn('NotFound', 'User accessed non-existent route:', location.pathname);
  }, [location.pathname]);

  return (
    <div className="flex min-h-screen items-center justify-center bg-muted">
      <div className="text-center">
        <h1 className="mb-4 text-4xl font-bold">{t('errors.notFound.code')}</h1>
        <p className="mb-4 text-xl text-muted-foreground">{t('errors.notFound.message')}</p>
        <a href="/" className="text-primary underline hover:text-primary/90">
          {t('errors.notFound.returnHome')}
        </a>
      </div>
    </div>
  );
};

export default NotFound;
